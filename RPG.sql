CREATE DATABASE DB_RPGS;

USE DB_RPGS;

CREATE TABLE TB_CLASSE(
ID BIGINT AUTO_INCREMENT,
NOME VARCHAR(255),
ARMA VARCHAR(255),
DESCRICAO VARCHAR (1000),
PRIMARY KEY(ID)
);

CREATE TABLE TB_PERSONAGEM(
ID BIGINT AUTO_INCREMENT,
NOME VARCHAR(255),
RACA VARCHAR(255),
ATAQUE DOUBLE,
DEFESA DOUBLE,
HP INT, 
PRIMARY KEY(ID),
CLASSE_ID BIGINT,
FOREIGN KEY(CLASSE_ID) REFERENCES TB_CLASSE(ID)
);

